#white space can be added for organization and aesthetics
#\t creates a tab
print("Python")
print("\tPython")

#\n creates a new line
print("maxwell \nloves \nrenae")

#combination of both
print("Love Letter: \n\tMaxwell \n\tLoves \n\tRenae!")

#you can use the rstrip() method to remove whitespace as necessary
favorite_language = "python "
print(favorite_language.rstrip())
print(favorite_language)